    public int Dem(){
        int dem =0;
        try {
            Connect cn = new Connect();
            ResultSet rs = cn.GetData("canbo");
            while(rs.next()){
                dem++;
            }
            cn.Close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QLCB.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(QLCB.class.getName()).log(Level.SEVERE, null, ex);
        }
        return dem;
    }
    public void Show_Table(){
        DefaultTableModel mod = (DefaultTableModel) tbCanBo.getModel();
        mod.setRowCount(Dem());
        try {
            Connect cn = new Connect();
            ResultSet rs = cn.GetData("canbo");
            int i =0;
            while(rs.next()){
                tbCanBo.setValueAt(rs.getString("MaCB"), i, 0);
                tbCanBo.setValueAt(rs.getString("Hoten"), i, 1);
                tbCanBo.setValueAt(rs.getString("Chucvu"), i, 2);
                tbCanBo.setValueAt(rs.getString("Hsluong"), i, 3);
                tbCanBo.setValueAt(rs.getString("Phucap"), i, 4);
                i++;
            }
            cn.Close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QLCB.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(QLCB.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
