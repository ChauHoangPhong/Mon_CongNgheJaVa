    private boolean check(String Ma){
        boolean check = false;
        try {
            Connect cn = new Connect();
            ResultSet rs = cn.GetData("canbo");
            while(rs.next()){
                if(rs.getString(1).equals(Ma)==true){
                    check = true;
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QLCB.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(QLCB.class.getName()).log(Level.SEVERE, null, ex);
        }
        return check;
    }
    public void Insert(){
        String MaCB = txtMaCB.getText();
        String Hoten = txtHoten.getText();
        String Chucvu = txtChucvu.getText();
        float Hsluong = Float.parseFloat(txtHsluong.getText());
        float Phucap = Float.parseFloat(txtPhucap.getText());
        try {
            Connect cn = new Connect();
            cn.Insert(MaCB, Hoten, Chucvu, Hsluong, Phucap);
            cn.Close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QLCB.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(QLCB.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String MaCB = txtMaCB.getText();
        String Hoten = txtHoten.getText();
        String Chucvu = txtChucvu.getText();
        String Hsluong = txtHsluong.getText();
        String Phucap = txtPhucap.getText();
        MaCB = MaCB.replaceAll("", "");
        Hoten = Hoten.replaceAll("", "");
        Chucvu = Chucvu.replaceAll("", "");
        Hsluong = Hsluong.replaceAll("", "");
        Phucap = Phucap.replaceAll("", "");
        if(txtMaCB.getText().equals("")==true||txtHoten.getText().equals("")==true||
                txtChucvu.getText().equals("")==true||txtHsluong.getText().equals("")==true||
                txtPhucap.getText().equals("")==true){
            JOptionPane.showMessageDialog(null,"Bạn chưa nhập đủ dữ liệu!!!");
        }else{
            if(check(MaCB)== true){
                JOptionPane.showMessageDialog(null,"Mã cán bộ bị trùng xin vui lòng nhập lại");
            }else{
                Insert();
                Show_Table();
                JOptionPane.showMessageDialog(null,"Bạn đã thêm thành công.");
                
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed
