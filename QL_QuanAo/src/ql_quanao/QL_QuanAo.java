    public int Dem(){
        int dem =0;
        try {
            Connect cn = new Connect();
            ResultSet rs = cn.GetData("quanao");
            while(rs.next()){
                dem++;
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Connect.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Connect.class.getName()).log(Level.SEVERE, null, ex);
        }
        return dem;
    }
    public void ShowTable(){
        DefaultTableModel mod = (DefaultTableModel) tbQuanAo.getModel();
        mod.setRowCount(Dem());
        try {
            Connect con = new Connect();
            ResultSet rs = con.GetData("quanao");
            int i=0;
            while(rs.next()){
                tbQuanAo.setValueAt(rs.getString("Ma"), i, 0);
                tbQuanAo.setValueAt(rs.getString("Loai"), i, 1);
                tbQuanAo.setValueAt(rs.getString("kthuoc"), i, 2);
                tbQuanAo.setValueAt(rs.getString("chatlieu"), i, 3);
                i++;
            }
            con.Close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QL_QuanAo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(QL_QuanAo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
