 public boolean check(String Ma){
     boolean check = false;
        try {
            Connect cn = new Connect();
            ResultSet rs = cn.GetData("quanao");
            while(rs.next()){
                if(rs.getString(1).equals(Ma)== true){
                    check = true;
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QL_QuanAo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(QL_QuanAo.class.getName()).log(Level.SEVERE, null, ex);
        }
        return check;
 }
