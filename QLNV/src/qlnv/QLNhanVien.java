     private boolean check(String Ma){
        boolean check = false;
        try {
            Connect cn = new Connect();
            ResultSet rs = cn.GetData("nhanvien");
            while(rs.next()){
                if(rs.getString(1).equals(Ma)==true){
                     check = true;
                }
               
            }
            cn.Close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        return check;
    }
     public void Them(){
        String Ma = txtMa.getText();
        String Hoten = txtTen.getText();
        String Que = txtQue.getText();
       float Hesoluong = Float.parseFloat(txtHeSoLuong.getText());
        try {
            Connect cn = new Connect();
            cn.Insert(Ma, Hoten, Que, Hesoluong);
            cn.Close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
   
    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        txtMa.setEnabled(true);
        String Ma = txtMa.getText();
        String Hoten = txtTen.getText();
        String Que = txtQue.getText();
        String Hesoluong = txtHeSoLuong.getText();
        
        Ma= Ma.replaceAll("", "");
        Hoten = Hoten.replaceAll("","");
        Que = Que.replaceAll("", "");
        Hesoluong = Hesoluong.replaceAll("", "");
        
        if(txtMa.getText().equals("")==true||txtTen.getText().equals("")==true||
                txtQue.getText().equals("")==true||txtHeSoLuong.getText().equals("")==true){
            JOptionPane.showMessageDialog(null,"Ban chua nhap du lieu");
        }else{
            if(check(Ma)== true){
                JOptionPane.showMessageDialog(null,"Ma nhan vien bi trung xin vui long nhap lai!");
            }else{
                Them();
                Show_Table();
                JOptionPane.showMessageDialog(null,"Ban da them thanh cong!!!");
            }
        }
                
    }//GEN-LAST:event_btnThemMouseClicked
