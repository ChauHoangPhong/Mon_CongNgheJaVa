    public void Clear_Table(){
        DefaultTableModel mod = (DefaultTableModel) tbNhanvien.getModel();
        mod.setRowCount(0);
        mod.setRowCount(Dem());
    }
    public void Timkiem(String Ten){
        Clear_Table();
        try {
            Connect cn = new Connect();
            ResultSet rs = cn.Timkiem(Ten);
            int i =0;
            while(rs.next()){
                tbNhanvien.setValueAt(rs.getString("MaNV"), i, 0);
                tbNhanvien.setValueAt(rs.getString("HoTen"), i, 1);
                tbNhanvien.setValueAt(rs.getString("Que"), i, 2);
                tbNhanvien.setValueAt(rs.getString("Hesoluong"), i, 3);
                i++;
            }
            cn.Close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(QLNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    } 
